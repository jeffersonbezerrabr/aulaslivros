#Escreva uma função que receba o lado de um quadrado e retorne sua área (A = lado²).

# Valores esperados:

# area_quadrado(4) == 16
# area_quadrado(9) == 81

def area_quadrado(a):
    return a ** 2

print(f"área_quadrado(4) == 16 -> obtido: {area_quadrado(4)}")
print(f"área_quadrado(9) == 81 -> obtido: {area_quadrado(9)}")

