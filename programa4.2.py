#Programa 4.2: Carro novo ou velho, dependendo da idade - Página 120

#Recebe a idade do carro do usuário
idade = int(input("Digite a idade do seu carro: "))

#Primeira comparação: Compara se a idade do carro é menor ou igual a 3
if idade <= 3:
    #Mostra o resultado
    print("Seu carro é novo")

#Segunda comparação: Compara se a idade do carro é maior que 3
if idade > 3:
    #Mostra o resultado
    print("Seu carro é velho")
    